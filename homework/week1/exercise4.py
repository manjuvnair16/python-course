#push:   The git push command is used to upload local repository content to a remote repository. 

#commit: The git commit command creates a commit, which is like a snapshot of your repository. Commits include lots of metadata in addition to the contents and message, like the author, timestamp, and more.

#pull:   The git pull command is used to fetch and download content from a remote repository and immediately update the local repository to match that content. The git pull command is actually a combination of two other commands, git fetch followed by git merge.


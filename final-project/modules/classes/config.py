        self.NO_OF_MONSTERS_TO_BE_KILLED_MAX_FOR_LEVEL_ONE = config_data["MONSTERS_KILLED_LIMITS_FOR_DIFFERENT_LEVELS"]["NO_OF_MONSTERS_TO_BE_KILLED_MAX_FOR_LEVEL_ONE"]
